#!/usr/bin/env node
// Regenerates src/lib/testStats.ts from the current test inventory.
// Run with `pnpm stats:refresh` whenever specs change.
import { execSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(fileURLToPath(import.meta.url), '../..');
const OUT = path.join(ROOT, 'src', 'lib', 'testStats.ts');

function sh(cmd) {
  return execSync(cmd, { cwd: ROOT, encoding: 'utf-8', stdio: ['ignore', 'pipe', 'inherit'] });
}

function countPwSpecs(node) {
  let n = 0;
  for (const s of node.specs ?? []) n += (s.tests ?? []).length;
  for (const c of node.suites ?? []) n += countPwSpecs(c);
  return n;
}

const pwJson = JSON.parse(sh('pnpm exec playwright test --list --reporter=json'));
const e2e = (pwJson.suites ?? []).reduce((acc, s) => acc + countPwSpecs(s), 0);
const e2eFiles = (pwJson.suites ?? []).length;
const projects = (pwJson.config?.projects ?? []).length;

const engineFamilies = new Set();
for (const p of pwJson.config?.projects ?? []) {
  const ua = (p.use?.defaultBrowserType ?? '').toLowerCase();
  if (ua) engineFamilies.add(ua);
}
const engines = engineFamilies.size || 3;

const vitestList = sh('pnpm exec vitest list').trim().split('\n').filter(Boolean);
const unit = vitestList.length;
const unitFiles = new Set(vitestList.map((l) => l.split(' > ')[0])).size;

const body = `// Generated by scripts/refresh-test-stats.mjs — do not hand-edit.
// Run \`pnpm stats:refresh\` to regenerate after spec changes.
export const TEST_STATS = {
  unit: ${unit},
  unitFiles: ${unitFiles},
  e2e: ${e2e},
  e2eFiles: ${e2eFiles},
  projects: ${projects},
  engines: ${engines},
} as const;

export type TestStats = typeof TEST_STATS;
`;

writeFileSync(OUT, body);
console.log(`wrote ${path.relative(ROOT, OUT)}: unit=${unit}, e2e=${e2e}, projects=${projects}, engines=${engines}`);
